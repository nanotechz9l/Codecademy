=begin

'puts' and 'print'

The print command just takes whatever you give it and prints it to the screen. puts (for "put string") is slightly different: 
it adds a new (blank) line after the thing you want it to print. 

You use them like this:

puts "What's up?"
print "Oxnard Montalvo"

No parentheses or semicolons needed!


